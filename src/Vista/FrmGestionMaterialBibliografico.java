    private void btnRegistrarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarMaterialActionPerformed
        // TODO add your handling code here:

        if (cbTipoMaterial.getSelectedIndex() == 0) {

            JOptionPane.showMessageDialog(null, "Selecciona algun usuario y llena los campos");

        } else if (cbTipoMaterial.getSelectedIndex() == 1) {

            if (txtAsignaturaMaterial.getText().isEmpty()
                    || txtIsbnOIssnMaterial.getText().isEmpty()
                    || txtTituloMaterial.getText().isEmpty()
                    || txtAñoMaterial.getText().isEmpty()
                    || txtEditorialMaterial.getText().isEmpty()
                    || txtAutores.getText().isEmpty()
                    || txtNoCopiasMaterial.getText().isEmpty()
                    || txtNoPaginas.getText().isEmpty()
                    || cbDiasPrestamo.getSelectedIndex() == 0
                    || txtEdicionMaterial.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Llene todos los campos por favor");

            } else {

                String asignaturaMaterial,
                        tituloMaterial,
                        editorialMaterial,
                        autoresLibro,
                        edicionlibro;

                int añoMaterial = 0, noCopiasMaterial = 0, noPaginasLibro = 0, isbnoissnMaterial = 0, diasPrestamoLibro = 0;

                asignaturaMaterial = txtAsignaturaMaterial.getText();
                isbnoissnMaterial = Integer.parseInt(txtIsbnOIssnMaterial.getText());
                tituloMaterial = txtTituloMaterial.getText();
                editorialMaterial = txtEditorialMaterial.getText();
                autoresLibro = txtAutores.getText();
                edicionlibro = txtEdicionMaterial.getText();
                añoMaterial = Integer.parseInt(txtAñoMaterial.getText());
                noCopiasMaterial = Integer.parseInt(txtNoCopiasMaterial.getText());
                noPaginasLibro = Integer.parseInt(txtNoPaginas.getText());

                switch (cbDiasPrestamo.getSelectedIndex()) {
                    case 1:
                        diasPrestamoLibro = 2;
                        break;
                    case 2:
                        diasPrestamoLibro = 5;
                        break;
                }

                Libro libro = new Libro(autoresLibro, edicionlibro, noPaginasLibro, diasPrestamoLibro, tituloMaterial, editorialMaterial, añoMaterial, asignaturaMaterial, noCopiasMaterial, isbnoissnMaterial);

                if (controladorl.verificarLibro(libro.getIsbnoIssn())
                        && controladorr.verificarRevista(libro.getIsbnoIssn())) {

                    if (controladorl.registrarLibro(libro)) {
                        JOptionPane.showMessageDialog(null, "El libro se ha registrado ");
                        limpiarCampos();
                        listarTablas();
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al registrar el libro");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Ya hay un material bibliografico con ese código");

                }

            }

        } else {

            if (txtAsignaturaMaterial.getText().isEmpty()
                    || txtIsbnOIssnMaterial.getText().isEmpty()
                    || txtTituloMaterial.getText().isEmpty()
                    || txtAñoMaterial.getText().isEmpty()
                    || txtEditorialMaterial.getText().isEmpty()
                    || txtVolumenMaterial.getText().isEmpty()
                    || txtNoCopiasMaterial.getText().isEmpty()
                    || txtNumeroMaterial.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Llene todos los campos por favor");

            } else {

                String asignaturaMaterial,
                        tituloMaterial,
                        editorialMaterial,
                        volumen;

                int añoMaterial = 0, noCopiasMaterial = 0, numero = 0, isbnoissnMaterial = 0;

                asignaturaMaterial = txtAsignaturaMaterial.getText();
                isbnoissnMaterial = Integer.parseInt(txtIsbnOIssnMaterial.getText());
                tituloMaterial = txtTituloMaterial.getText();
                editorialMaterial = txtEditorialMaterial.getText();
                volumen = txtVolumenMaterial.getText();
                añoMaterial = Integer.parseInt(txtAñoMaterial.getText());
                noCopiasMaterial = Integer.parseInt(txtNoCopiasMaterial.getText());
                numero = Integer.parseInt(txtNumeroMaterial.getText());

                Revista revista = new Revista(volumen, numero, tituloMaterial, editorialMaterial, añoMaterial, asignaturaMaterial, noCopiasMaterial, isbnoissnMaterial);

                if (controladorl.verificarLibro(revista.getIsbnoIssn())
                        && controladorr.verificarRevista(revista.getIsbnoIssn())) {

                    if (controladorr.registrarRevista(revista)) {
                        JOptionPane.showMessageDialog(null, "La revista se ha registrado ");
                        limpiarCampos();
                        listarTablas();
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al registrar la revista");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Ya hay un material bibliografico con ese código");

                }
            }

        }


    }//GEN-LAST:event_btnRegistrarMaterialActionPerformed

