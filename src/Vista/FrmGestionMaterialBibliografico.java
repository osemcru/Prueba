    private void btnEliminarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMaterialActionPerformed
        // TODO add your handling code here:

        int codigo = Integer.parseInt(JOptionPane.showInputDialog("Introduzca la isbn o issn del material bibliografico a eliminar"));

        if (controladorl.eliminarLibro(codigo)) {
            JOptionPane.showMessageDialog(null, "Eliminado Correctamente el libro",
                    "Informe", JOptionPane.INFORMATION_MESSAGE);
        } else if (controladorr.eliminarRevista(codigo)) {
            JOptionPane.showMessageDialog(null, "Eliminada Correctamente la revista",
                    "Informe", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "No se ha podido eliminar");
        }

        listarTablas();
    }//GEN-LAST:event_btnEliminarMaterialActionPerformed

    private void btnBuscarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarMaterialActionPerformed
        // TODO add your handling code here:

        int codigo = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la isbn o isnn del material bibliografico"));
        Libro libro = controladorl.buscarLibro(codigo);
        Revista revista = controladorr.buscarRevista(codigo);

        if (libro != null) {
            cbTipoMaterial.setSelectedIndex(1);
            cbTipoMaterial.setEnabled(false);
            txtAsignaturaMaterial.setText(libro.getAsignatura());
            txtIsbnOIssnMaterial.setText(String.valueOf(libro.getIsbnoIssn()));
            txtTituloMaterial.setText(libro.getTitulo());
            txtEditorialMaterial.setText(libro.getEditorial());
            txtAutores.setText(libro.getAutor());
            txtEdicionMaterial.setText(libro.getEdicion());
            txtAñoMaterial.setText(String.valueOf(libro.getAño()));
            txtNoCopiasMaterial.setText(String.valueOf(libro.getNumCopias()));
            txtNoPaginas.setText(String.valueOf(libro.getNumeroPaginas()));
            txtIsbnOIssnMaterial.setEditable(false);
            btnModificarMaterial.setEnabled(true);
            btnCancelarMaterial.setVisible(true);
            btnRegistrarMaterial.setEnabled(false);

            if (libro.getNumeroDiasPrestado() == (2)) {
                cbDiasPrestamo.setSelectedIndex(1);
            } else if (libro.getNumeroDiasPrestado() == (5)) {
                cbDiasPrestamo.setSelectedIndex(2);
            }

        } else if (revista != null) {
            cbTipoMaterial.setSelectedIndex(2);
            cbTipoMaterial.setEnabled(false);
            txtAsignaturaMaterial.setText(revista.getAsignatura());
            txtIsbnOIssnMaterial.setText(String.valueOf(revista.getIsbnoIssn()));
            txtTituloMaterial.setText(revista.getTitulo());
            txtEditorialMaterial.setText(revista.getEditorial());
            txtVolumenMaterial.setText(revista.getVolumen());
            txtAñoMaterial.setText(String.valueOf(revista.getAño()));
            txtNoCopiasMaterial.setText(String.valueOf(revista.getNumCopias()));
            txtNumeroMaterial.setText(String.valueOf(revista.getNumero()));
            txtIsbnOIssnMaterial.setEditable(false);
            btnModificarMaterial.setEnabled(true);
            btnCancelarMaterial.setVisible(true);
            btnRegistrarMaterial.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "No se ha podido encontrar el material");
        }


    }//GEN-LAST:event_btnBuscarMaterialActionPerformed

